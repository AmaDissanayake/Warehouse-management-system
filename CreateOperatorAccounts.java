    public void Update_login_table()
    {
        int log;
        
         try {
            DBconnector db = new DBconnector();
            Connection con = db.getConnection();
           
            PreparedStatement pre1 = null;
            pre1 = con.prepareStatement("SELECT * FROM `logincredentials`");
            ResultSet R_set  = pre1.executeQuery();
            
            ResultSetMetaData result = R_set.getMetaData();
            log = result.getColumnCount();
            DefaultTableModel Deftbl = (DefaultTableModel)jTableoperator.getModel();
            Deftbl.setRowCount(0);
            
            while(R_set.next())
            {
                Vector table = new Vector();
                for (int i=1; i<=log; i++)
                {
                   table.add(R_set.getString("LID")); 
                   table.add(R_set.getString("User_ID"));
                   table.add(R_set.getString("User_Name"));
                   table.add(R_set.getString("NIC"));
                   table.add(R_set.getString("Phone_Number"));
                   table.add(R_set.getString("User_Handle"));
                   table.add(R_set.getString("Password"));
                }
                
                Deftbl.addRow(table);
                
            }
          
            
        } catch (SQLException ex) {
            Logger.getLogger(CreateOperatorAccounts.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
