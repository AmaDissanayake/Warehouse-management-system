    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        
        String UserID = txt_userID.getText();
        String UserName = txt_username.getText();
        String NIC = txt_NIC.getText();
        String PhoneNumber = txt_PhnNO.getText();
        String UserHandle = cmb_Userhandle.getSelectedItem().toString();
        String Password = txt_pw.getText();
        
       if(UserID.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Insert The User ID");
        }
        
        else if(UserName.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Insert The Username");
        }
        
        else if(NIC.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Insert The NIC");
        }
        
        else if(PhoneNumber.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Insert The Phone Number");
        }
        
        else if(Password.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Insert The Password");
        }
        else { 
            
            PreparedStatement stmt;
      String query = "INSERT INTO `logincredentials`(`User_ID`, `User_Name`, `NIC`, `Phone_Number`, `User_Handle`, `Password`) VALUES (?,?,?,?,?,?)";
      
        try {
            stmt = DBconnector.getConnection().prepareStatement(query);
            
            stmt.setString(1, UserID);
            stmt.setString(2, UserName);
            stmt.setString(3, NIC);
            stmt.setString(4, PhoneNumber);
            stmt.setString(5, UserHandle);
            stmt.setString(6, Password);
            
            if(stmt.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null,"User Added");
            }
            
        txt_userID.setText("");
        txt_username.setText("");
        txt_NIC.setText("");
        txt_PhnNO.setText("");
        txt_pw.setText("");
        txtuid.setText("");
        txt_userID.requestFocus();
            
           Update_login_table();
            
        } catch (SQLException ex) {
            Logger.getLogger(CreateOperatorAccounts.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        
        
        
    }//GEN-LAST:event_btn_saveActionPerformed

