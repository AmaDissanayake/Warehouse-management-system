         DefaultTableModel del = (DefaultTableModel)tableStorageJobINfoclose.getModel();
        
        int select = tableStorageJobINfoclose.getSelectedRow();
        
        String id = del.getValueAt(select,0).toString();

        int  res = JOptionPane.showConfirmDialog(this,"Aye you Sure to Delete The Account?","Warning",JOptionPane.YES_NO_OPTION);

        if(res == JOptionPane.YES_OPTION)
        {
            try {
                
                DBconnector db = new DBconnector();
                Connection con = db.getConnection();


                PreparedStatement pstdel;
                pstdel = con.prepareStatement("DELETE FROM `logincredentials` WHERE `User_ID`=?");

                pstdel.setString(1,id);
                pstdel.executeUpdate();

                JOptionPane.showMessageDialog(this, "Account Record Deleted");
                Update_close_storage_Job_table();

            } catch (SQLException ex) {
                Logger.getLogger(DeleteUsers.class.getName()).log(Level.SEVERE, null, ex);
            }
         
    }
       
